public class PrimitiveTypes {

    public static void main(String[] args) {
        // Primitive types
        // Integer types
        byte b = 100;
        short s = 10000;
        int n = 1000000000;
        long l = 1000000000000000000L;

        // Floating point types
        float f = 10.5f;
        double d = 10.5;

        // Char
        char c = 'a';

        // Logical
        boolean bool = true;
    }
}
